var EN = {};
var currentLang = localStorage.getItem("ctj_lang") || "en";

/* Store original dynamic profile values once */
var PROFILE_EN = {
  age: null,
  level: null,
  avail: null,
  phone: null,
  ratingCount: null
};

function saveEnglish() {
  document.querySelectorAll("[data-key]").forEach(function(el) {
    var key = el.getAttribute("data-key");
    if (!EN[key]) EN[key] = el.textContent;
  });
  document.querySelectorAll("[data-key-placeholder]").forEach(function(el) {
    var key = el.getAttribute("data-key-placeholder");
    if (!EN[key]) EN[key] = el.placeholder;
  });

  /* Save original English profile values on first load */
  var ageEl    = document.getElementById("profileAge");
  var levelEl  = document.getElementById("profileLevel");
  var availEl  = document.getElementById("profileAvail");
  var phoneEl  = document.getElementById("profilePhoneType");
  var rcEl     = document.getElementById("ratingCount");
  if (ageEl   && !PROFILE_EN.age)         PROFILE_EN.age         = ageEl.textContent;
  if (levelEl && !PROFILE_EN.level)       PROFILE_EN.level       = levelEl.textContent;
  if (availEl && !PROFILE_EN.avail)       PROFILE_EN.avail       = availEl.textContent;
  if (phoneEl && !PROFILE_EN.phone)       PROFILE_EN.phone       = phoneEl.textContent;
  if (rcEl    && !PROFILE_EN.ratingCount) PROFILE_EN.ratingCount = rcEl.textContent;
}

function applyLanguage() {
  var lang = currentLang === "bn" ? BN : EN;

  var btn = document.getElementById("langToggle");
  if (btn) btn.textContent = currentLang === "en" ? "বাংলা" : "English";

  document.querySelectorAll("[data-key]").forEach(function(el) {
    var key = el.getAttribute("data-key");
    if (lang[key]) el.textContent = lang[key];
  });
  document.querySelectorAll("[data-key-placeholder]").forEach(function(el) {
    var key = el.getAttribute("data-key-placeholder");
    if (lang[key]) el.placeholder = lang[key];
  });

  applyProfileDynamic();
}

function applyProfileDynamic() {
  var levelNames_BN = {
    "Newcomer":"Newcomer2","Rising":"Rising2","Experienced":"Experienced2",
    "Trusted":"Trusted2","Expert":"Expert2","Master":"Master2"
  };

  var ageEl   = document.getElementById("profileAge");
  var levelEl = document.getElementById("profileLevel");
  var availEl = document.getElementById("profileAvail");
  var phoneEl = document.getElementById("profilePhoneType");
  var rcEl    = document.getElementById("ratingCount");

  if (currentLang === "en") {
    /* Restore original English values */
    if (ageEl   && PROFILE_EN.age)         ageEl.textContent   = PROFILE_EN.age;
    if (levelEl && PROFILE_EN.level)       levelEl.textContent = PROFILE_EN.level;
    /* Availability: restore from class since EN text may not be saved yet */
    if (availEl) {
      if (availEl.classList.contains("available"))   availEl.textContent = "Available";
      else if (availEl.classList.contains("unavailable")) availEl.textContent = "Currently Busy";
    }
    if (phoneEl && PROFILE_EN.phone)       phoneEl.textContent = PROFILE_EN.phone;
    if (rcEl    && PROFILE_EN.ratingCount) rcEl.textContent    = PROFILE_EN.ratingCount;
    return;
  }

  /* Apply BN -- always work from saved English originals */
  if (ageEl && PROFILE_EN.age) {
    ageEl.textContent = PROFILE_EN.age.replace("Age:", "Age2:");
  }
  if (levelEl && PROFILE_EN.level) {
    var t = PROFILE_EN.level;
    Object.keys(levelNames_BN).forEach(function(eng) {
      t = t.replace(eng, levelNames_BN[eng]);
    });
    t = t.replace("Level", "Level2");
    levelEl.textContent = t;
  }
  if (availEl) {
    if (availEl.classList.contains("available"))   availEl.textContent = "Available2";
    else if (availEl.classList.contains("unavailable")) availEl.textContent = "Currently Busy2";
  }
  if (phoneEl && PROFILE_EN.phone) {
    phoneEl.textContent = PROFILE_EN.phone === "Basic Phone" ? "Basic Phone2" : PROFILE_EN.phone;
  }
  if (rcEl && PROFILE_EN.ratingCount) {
    rcEl.textContent = PROFILE_EN.ratingCount
      .replace("Based on", "Based on2")
      .replace("reviews", "reviews2");
  }
}

function toggleLanguage() {
  currentLang = currentLang === "en" ? "bn" : "en";
  localStorage.setItem("ctj_lang", currentLang);
  applyLanguage();
}

document.addEventListener("DOMContentLoaded", function() {
  saveEnglish();
  applyLanguage();
});